//? To find the area of a reactangle in javascript,you can create a function that takes the length and width as parameters and applies the formula.

function findArea(length, width) {
  let area = length * width;
  return area;
}
console.log(findArea(5, 10)); // Output: 50
console.log(findArea(10, 5.5)); // Output: 55
