//*------- find the average of two number

//? To find the average of two numbers in javascript,you can add the two numbers together and then divide the sum by 2.

function averageOfTwoNumbers(num1, num2) {
  let sum = num1 + num2;
  let average = sum / 2;
  return average;
}
console.log(averageOfTwoNumbers(10, 20)); // Output: 15
